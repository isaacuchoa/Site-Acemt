<p>O médico não pode ser perito de paciente para quem preste atendimento como assistente, mesmo que o faça em entidade pública. Os atestados médicos só podem ser homologados quando o médico perito e/ou membro de junta médica examinar diretamente o paciente, sob pena de infringir os postulados éticos da profissão. Se o regulamento estabelece que a junta médica será composta por três membros, não tem validade o ato praticado por apenas dois profissionais, por não preencher os requisitos formais. Não pode um membro da junta médica assinar posteriormente o laudo como se tivesse efetivamente participado do ato. Nada impede que por ordem administrativa a inspeção seja realizada após recomposta a junta médica. Porém, o laudo pericial exarado pela mesma deve conter rigorosamente os dados clínicos e patológicos constatados na data em que o paciente foi submetido à perícia. Em hipótese nenhuma o médico pode se submeter a imposições administrativas que contrariem os postulados éticos de sua profissão</p>
