<h3>SUGESTÃO DE CONDUTA MÉDICO ADMINISTRATIVA-SCMA N0 02/2004</h3><br/>
<p>Atuação do Médico do Trabalho, Coordenador do PCMSO<br/>
e, ou Encarregado pelos exames do PCMSO, empregado ou<br/>
ex-empregado da empresa, contratado ou ex-contratado pela<br/>
empresa, como Assistente Técnico.</p><br/>
<h3>EMENTA:</h3><br/>
<p>1 – Não há como o Médico do Trabalho
coordenador do PCMSO e, ou encarregado pelos<br/>
exames ocupacionais, empregado ou contratado
pela empresa atuar na função de Assistente<br/>
Técnico da empresa ou de empregado dessa
empresa sem que haja prejuízo na sua relação<br/>
com as partes (empresa ou empregado).</p>
<p>2 – O Médico do Trabalho não deve
atuar na função de Assistente Técnico da<br/>
empresa ou de trabalhador da empresa da qual
tenha sido Médico Examinador ou Coordenador
do PCMSO.</p>
